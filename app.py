# to display another " middle of the string
message = "Python \"Programming"
print(message)
# to display ' middle of the string
message = "Python \'Programming"
print(message)
# to display \ middle of the string
message = "Python \\Programming"
print(message)
# for new line
message = "Python \nProgramming"
print(message)
# better to use """ """ instead of n\ otherwise that's look ugly
message = """Python
Programming
"""
print(message)
